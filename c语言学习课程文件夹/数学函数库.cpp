#include <stdio.h>
#include <math.h>
#include <stdlib.h>

int main (){
    printf("pow(2,3) = %lf\n", pow(2,3));
    printf("sqrt = %lf\n", sqrt(2));
    printf("ceil(4.01) = %lf\n",ceil(4.01));
    printf("floor(4.99) = %lf\n",floor(4.99));
    printf("abs(-4) = %d\n",abs(-4));
    printf("fabs(-4.6) = %lf\n",fabs(-4.6));
    printf("log(9) = %lf\n",log(9));
    printf("acos(-1) =  %.9lf\n",acos(-1));
    return 0;
}
